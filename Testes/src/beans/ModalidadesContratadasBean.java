package beans;

public class ModalidadesContratadasBean {
	
	// Atributos
	private int idModalidade;
	private int idAluno;
	private int dataDeContratacao;
	
	
	// Metodos
	/**
	 * @return the idModalidade
	 */
	public int getIdModalidade() {
		return idModalidade;
	}
	/**
	 * @param idModalidade the idModalidade to set
	 */
	public void setIdModalidade(int idModalidade) {
		this.idModalidade = idModalidade;
	}
	/**
	 * @return the idAluno
	 */
	public int getIdAluno() {
		return idAluno;
	}
	/**
	 * @param idAluno the idAluno to set
	 */
	public void setIdAluno(int idAluno) {
		this.idAluno = idAluno;
	}
	/**
	 * @return the dataDeContratacao
	 */
	public int getDataDeContratacao() {
		return dataDeContratacao;
	}
	/**
	 * @param dataDeContratacao the dataDeContratacao to set
	 */
	public void setDataDeContratacao(int dataDeContratacao) {
		this.dataDeContratacao = dataDeContratacao;
	}
	
	

}
