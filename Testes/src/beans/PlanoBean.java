package beans;

public class PlanoBean {

	// Atributos
	
	private int idPlano;
	private String planoDescricao;
	private int statusPlano;
	
	// Metodos
	
	/**
	 * @return the idPlano
	 */
	public int getIdPlano() {
		return idPlano;
	}
	/**
	 * @param idPlano the idPlano to set
	 */
	public void setIdPlano(int idPlano) {
		this.idPlano = idPlano;
	}
	/**
	 * @return the planoDescricao
	 */
	public String getPlanoDescricao() {
		return planoDescricao;
	}
	/**
	 * @param planoDescricao the planoDescricao to set
	 */
	public void setPlanoDescricao(String planoDescricao) {
		this.planoDescricao = planoDescricao;
	}
	/**
	 * @return the statusPlano
	 */
	public int getStatusPlano() {
		return statusPlano;
	}
	/**
	 * @param statusPlano the statusPlano to set
	 */
	public void setStatusPlano(int statusPlano) {
		this.statusPlano = statusPlano;
	}


	
}
